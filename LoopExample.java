public class LoopExample {
    public static void main(String[] args) {
        // Initialization conditions iteration
        for (int i = 0; i<10; i++) {
            System.out.println("Hi");
        }
        int count = 10;
        while (count < 10) {
            System.out.println("Hi"); 
            count++;
        }
    }
}